async function seedDatabase() {
  try {
    await mongoose.connect(MONGODB_URI, {
      serverSelectionTimeoutMS: 15000,
      connectTimeoutMS: 20000,
    });
    console.log('Connected to MongoDB.');

    await Question.deleteMany({});
    console.log('Cleared existing questions.');

    await Question.insertMany(questions);
    console.log(`Successfully inserted ${questions.length} questions!`);
    
    const countGen = questions.filter(q => q.category === 'general').length;
    const countSci = questions.filter(q => q.category === 'science').length;
    const countHis = questions.filter(q => q.category === 'history').length;
    console.log(`General: ${countGen}, Science: ${countSci}, History: ${countHis}`);

  } catch (error) {
    console.error('Error seeding database:', error);
  } finally {
    await mongoose.disconnect();
    console.log('Disconnected from MongoDB.');
  }
}

seedDatabase();
